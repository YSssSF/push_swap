#include "push_swap.h"

void	check_args(char **av)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (av[i])
	{
		j = 0;
		if (av[i][j] == '+' || av[i][j] == '-')
			j++;
		if ((av[i][j - 1] == '+' && av[i][j] == '\0')
			|| (av[i][j - 1] == '-' && av[i][j] == '\0'))
			ft_error();
		while (av[i][j])
		{
			if (!(av[i][j] >= '0' && av[i][j] <= '9'))
				ft_error();
			j++;
		}
		i++;
	}
}
